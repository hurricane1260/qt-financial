package com.qtjf.tpa.jdt.bean;

public class UserParam {
	private UserInfo application;
	private String dk;
	private String token;

	public UserInfo getApplication() {
		return application;
	}

	public void setApplication(UserInfo application) {
		this.application = application;
	}

	public String getDk() {
		return dk;
	}

	public void setDk(String dk) {
		this.dk = dk;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

}
